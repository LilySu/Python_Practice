# Swapping is used

def sort(nums):
    # go in reverse from 0 to last index
    for i in range(len(nums)-1, 0, -1):
        # after each iteration, you will get max value in the end
        # that's why the range goes to i
        for j in range(i):
            if nums[j] > nums[j + 1]:
                temp = nums[j]
                nums[j] = nums[j + 1]
                nums[j + 1] = temp
    return nums

nums = [5,3,8,6,7,2]
sort(nums)
